function setup_experiment(exp_type)
% Create all experimental stimuli and settings

nTex=60; % number of textures
nTrials=300; % number of trials in each level
nLevels=4; % number of eccentricity levels
nSessions=4; % number of sessions to divide this into

% generate stimuli
[stimuli, seeds, tex, coords]=experiment.discriminate.prep.generate_stimuli(exp_type);

exp_settings = struct(...
    'exp_type', exp_type,...
    'stimuli', stimuli,...
    'tex', {tex},...
    'coords', {coords},...
    'seeds', seeds,...
    'ecc', [0 1.6 5 11.5],... % fixation eccentricities
    'stim_size', 64,...
    'nTex', nTex,...
    'nLevels', nLevels,...
    'nTrials', nTrials,...
    'nSessions', nSessions, ...
    'loadSessionStimuli', @experiment.discriminate.run.loadStimuli, ...
    'luminance', 0.5,...
    'contrast', 0.20,...
    'ppd', 60,...
    'moniter_brightness', 50,...
    'monitor_contrast', 100,...
    'monitorMaxPix', 255, ...
    'bgPixVal', 0.5*255, ...
    'stimulusIntervalMs', 200,...
    'responseIntervalMs', 1000, ...
    'fixationIntervalMs', 100,...
    'blankIntervalMs', 0 ...
    );

folderOut= ['exp_files/' exp_type];
mkdir(folderOut);
save([folderOut '/exp_settings.mat'], 'exp_settings');

end
